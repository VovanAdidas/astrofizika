// Функция для отображения информации о планете
function showInfo(planet) {
    // Получаем элемент для отображения информации
    const infoBox = document.getElementById('planet-info');
    let infoContent;

    // В зависимости от выбранной планеты, отображаем информацию
    switch (planet) {
        case 'Mercury':
            infoContent = `
                <h2>Меркурий</h2>
                <p>Меркурий — ближайшая к Солнцу планета. У неё нет атмосферы, и поверхность сильно нагревается днем и замерзает ночью.</p>
            `;
            break;
        case 'Venus':
            infoContent = `
                <h2>Венера</h2>
                <p>Венера — планета с самой плотной атмосферой в Солнечной системе, которая состоит в основном из углекислого газа. Поверхность Венеры горячая и покрыта вулканами.</p>
            `;
            break;
        case 'Earth':
            infoContent = `
                <h2>Земля</h2>
                <p>Земля — единственная известная планета, на которой есть жизнь. Она имеет разнообразную атмосферу и воду в жидком состоянии.</p>
            `;
            break;
        case 'Mars':
            infoContent = `
                <h2>Марс</h2>
                <p>Марс — известен как "красная планета" из-за своей окраски. Исследования показывают, что на Марсе могла существовать вода в прошлом.</p>
            `;
            break;
        case 'Jupiter':
            infoContent = `
                <h2>Юпитер</h2>
                <p>Юпитер — самая большая планета в Солнечной системе. У него есть многочисленные спутники, и он обладает мощным магнитным полем.</p>
            `;
            break;
        case 'Saturn':
            infoContent = `
                <h2>Сатурн</h2>
                <p>Сатурн известен своими кольцами, которые состоят из льда и камней. Это газовый гигант, и на нем есть много спутников.</p>
            `;
            break;
        case 'Uranus':
            infoContent = `
                <h2>Уран</h2>
                <p>Уран — планета, которая вращается на боку. Это ледяной гигант, окружённый системой кольцевых образований.</p>
            `;
            break;
        case 'Neptune':
            infoContent = `
                <h2>Нептун</h2>
                <p>Нептун — планета с сильнейшими ветрами в Солнечной системе. Он обладает красивыми кольцами и имеет несколько спутников.</p>
            `;
            break;
        default:
            infoContent = `<p>Кликните на планету, чтобы узнать о ней больше.</p>`;
    }

    // Обновляем содержимое блока с информацией о планете
    infoBox.innerHTML = infoContent;
}

